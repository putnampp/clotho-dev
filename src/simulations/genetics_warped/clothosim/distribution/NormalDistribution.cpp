#include "NormalDistribution.h"

#include <gsl/gsl_randist.h>

NormalDistribution::NormalDistribution( double sigma, double mean ) : 
    m_rng( NULL ),
    m_mu ( mean ),
    m_sigma( sigma ) {
}

NormalDistribution::~NormalDistribution() { }

void NormalDistribution::setRandomNumberGenerator( gsl_rng * rng ) {
    m_rng = rng;
}

double NormalDistribution::nextVariate() {
    if( !m_rng ) return 0.0;

    double var = gsl_ran_gaussian( m_rng, m_sigma );
    var += m_mu;

    return var;
}
